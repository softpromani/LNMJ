            <h3>About the Department</h3>
            <p class="p3"><span class="s1"> The University Department of Biotechnology, established in 1997, is running in the University Department of Botany. It is an interdisciplinary course and students passing out from Botany, Zoology Industrial Microbiology, Biotechnology and Chemistry can get themselves admitted.</span></p>

            <ul class="ul1">
                 <li class="li3"><span class="s1">The University Department of Biotechnology was established in December, 1996.</span></li>
                 <li class="li3"><span class="s1">However M.Sc. teaching programme started from academic session 1997 – 1999.</span></li>
                 <li class="li3"><span class="s1">But it was discontinued on technical grounds after session 2000 – 2002.</span></li>
                 <li class="li3"><span class="s1">With receipt of “No Objection Certificate” from Govt. of Bihar, teaching of Biotechnology was once again started from session 2006 - 2008.</span></li>
                 <li class="li3"><span class="s1">University Department of Biotechnology is located in the building of University Department of Botany. </span></li>
                 <li class="li3"><span class="s1">The founder Head was Prof. B. Jha and since then his position was held by the teachers like Prof. (Dr). Jayakar Jha followed by Prof. (Dr.) I. N .Mishra.</span></li>
            </ul>
